void NK5_set_XY(unsigned char X, unsigned char Y)
{
	 NK5_write_byte(0x40 | Y, 0);// column
	 NK5_write_byte(0x80 | X, 0);// row
}

void NK5_clear(void)
{
	 unsigned char t;
	 unsigned char k;
	 NK5_set_XY(0,0);
	 for(t=0;t<6;t++)
	 for(k=0;k<84;k++)
			NK5_write_byte(0x00,1);
}



