void NK5_write_byte(unsigned char dt, unsigned char command)
{
	 unsigned char i;
	 NK5_CE=0; 
	 NK5_DC=command; 

	 for(i=0;i<8;i++)
	 {
	  if(dt&0x80)
	   NK5_DIN=1;
	  else
	   NK5_DIN=0;
	  dt=dt<<1;  
	  NK5_CLK=0;   
	  NK5_CLK=1;
	 } 
	 NK5_DC=1; 
	 NK5_CE=1; 
	 NK5_DIN=1;
	 delayus(200);

}


